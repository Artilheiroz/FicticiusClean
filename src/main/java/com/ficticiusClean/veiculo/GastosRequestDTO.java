package com.ficticiusClean.veiculo;

public class GastosRequestDTO {
	
	private Double precoGasolina;
	private Float kmCidade;
	private Float kmRodovia;
	
	public Double getPrecoGasolina() {
		return precoGasolina;
	}
	public void setPrecoGasolina(Double precoGasolina) {
		this.precoGasolina = precoGasolina;
	}
	public Float getKmCidade() {
		return kmCidade;
	}
	public void setKmCidade(Float kmCidade) {
		this.kmCidade = kmCidade;
	}
	public Float getKmRodovia() {
		return kmRodovia;
	}
	public void setKmRodovia(Float kmRodovia) {
		this.kmRodovia = kmRodovia;
	}
	

}
